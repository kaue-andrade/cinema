    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        
        String codigo = txtCodigo.getText();
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        String telefone = txtEmail.getText();
        String email = txtTelefone.getText();
        
        if(!codigo.equals("") && !nome.equals("") && !cpf.equals("") && !telefone.equals("") && !email.equals("")){
            DefaultTableModel tabelaUsuarios = (DefaultTableModel) tblUsuarios.getModel();
            
            Object[] Usuario = new Object[]{
                codigo,
                nome,
                cpf,
                telefone,
                email,
            };
            
            tabelaUsuarios.addRow(Usuario);
            
            LimparCampos();
            
            JOptionPane.showMessageDialog(this, "Usuário cadastrado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Os campos Código, Nome, CPF, Telefone e E-Mail são obrigatórios.");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void LimparCampos(){
        txtCodigo.setText("");
        txtNome.setText("");
        txtCpf.setText("");
        txtEmail.setText("");
        txtTelefone.setText("");
    }
    
    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        
        int linhaSelecionada = tblUsuarios.getSelectedRow();
        
        if(linhaSelecionada != -1){
            String codigo = txtCodigo.getText();
            String nome = txtNome.getText();
            String cpf = txtCpf.getText();
            String telefone = txtEmail.getText();
            String email = txtTelefone.getText();
            
            if(!codigo.equals("") && !nome.equals("") && !cpf.equals("") && !telefone.equals("") && !email.equals("")){
                tblUsuarios.setValueAt(codigo, linhaSelecionada, 0);
                tblUsuarios.setValueAt(nome, linhaSelecionada, 1);
                tblUsuarios.setValueAt(cpf, linhaSelecionada, 2);
                tblUsuarios.setValueAt(telefone, linhaSelecionada, 3);
                tblUsuarios.setValueAt(email, linhaSelecionada, 4);
                
                LimparCampos();
                
                JOptionPane.showMessageDialog(this, "Cliente alterado com sucesso!");

            } else {
                JOptionPane.showMessageDialog(this, "Os campos Código, Nome, CPF, Telefone e E-Mail são obrigatórios.");
            }
        }
        
        else {
            JOptionPane.showMessageDialog(this, "Selecione uma linha para alteração.");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        
        int linhaSelecionada = tblUsuarios.getSelectedRow();
        
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(this, "Selecione uma linha para exclusão.");
        } else{
            DefaultTableModel tabelaUsuarios = (DefaultTableModel) tblUsuarios.getModel();
            tabelaUsuarios.removeRow(linhaSelecionada);
            LimparCampos();
            JOptionPane.showMessageDialog(this, "Cliente excluído com sucesso!");
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

